import numpy as np

def load_trajectory_data(file_name, pos_switch, att_switch):
    """
    Reads a trajectory text file (such as the one generated by TrajectoryGenerator.py)
    and converts the selected columns into a dictionary of NumPy arrays.
    
    Parameters:
    -----------
    file_name : str
        The path/name of the trajectory file (e.g., 'Trajectory.txt').
    pos_switch : str
        Filter for position columns. Options: 'ECR', 'LLA', or 'BOTH'.
    att_switch : str
        Filter for attitude columns. Options: 'EULER', 'QUATERNION', or 'BOTH'.
        
    Returns:
    --------
    dict
        A dictionary where keys are the column headers and values are NumPy arrays.
    """
    # Standardize switches to uppercase to prevent casing issues
    pos_switch = pos_switch.upper()
    att_switch = att_switch.upper()
    
    # Quick input validation
    if pos_switch not in {'ECR', 'LLA', 'BOTH'}:
        raise ValueError("pos_switch must be 'ECR', 'LLA', or 'BOTH'")
    if att_switch not in {'EULER', 'QUATERNION', 'BOTH'}:
        raise ValueError("att_switch must be 'EULER', 'QUATERNION', or 'BOTH'")
        
    # 1. Read the first line to extract the exact column headers
    with open(file_name, 'r') as fid:
        headers = fid.readline().strip().split()
        
    # 2. Load the numeric data grid (skipping the header row)
    data_matrix = np.loadtxt(file_name, skiprows=1)
    
    # 3. Define coordinate and attitude groupings based on TrajectoryGenerator.py definitions
    lla_set = {'Lat', 'Lon', 'Alt'}
    ecr_set = {'x', 'y', 'z'}
    euler_set = {'yaw', 'pitch', 'roll'}
    quat_set = {'q0', 'q1', 'q2', 'q3'}
    
    # 4. Determine columns to exclude based on the user's switches
    skip_columns = set()
    
    if pos_switch == 'LLA':
        skip_columns.update(ecr_set)
    elif pos_switch == 'ECR':
        skip_columns.update(lla_set)
        
    if att_switch == 'EULER':
        skip_columns.update(quat_set)
    elif att_switch == 'QUATERNION':
        skip_columns.update(euler_set)
        
    # 5. Populate the final dictionary with unskipped columns
    trajectory_dict = {}
    for i, header in enumerate(headers):
        if header not in skip_columns:
            # Map the header name to its corresponding column slice
            trajectory_dict[header] = data_matrix[:, i]
            
    return trajectory_dict
    
